package sudoku.logik.tipinfo;

/**
 * @author heroe
 * Ein Tiptext bestehend aus zwei Einheiten, zu dem Zweck, 
 * dass man diese in zwei Zeilen untereinander anzeigen kann bei Bedarf.
 */
public class EinTipText {
	public final String s1;
	public final String s2;

	public EinTipText(String s1, String s2) {
		this.s1 = s1;
		this.s2 = s2;
	}
}
